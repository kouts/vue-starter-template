<template>
  <nav class="navbar navbar-expand-md navbar-dark bg-dark fixed-top">
    <router-link to="/" class="navbar-brand" @click.native="collapseNavbar">Brand</router-link>
    <button
      :class="['navbar-toggler', !navbarExpanded && 'collapsed']"
      type="button"
      aria-controls="navbarDefault"
      :aria-expanded="navbarExpanded"
      aria-label="Toggle navigation"
      @click="toggleNavbar"
    >
      <span class="navbar-toggler-icon"></span>
    </button>

    <div :class="['collapse navbar-collapse', navbarExpanded && 'show']" id="navbarDefault">
      <ul class="navbar-nav mr-auto">
        <li class="nav-item">
          <router-link to="/" exact exact-active-class="active" class="nav-link" @click.native="collapseNavbar">Home</router-link>
        </li>
        <li class="nav-item">
          <router-link to="/about" class="nav-link" @click.native="collapseNavbar">About</router-link>
        </li>
      </ul>
    </div>
  </nav>
</template>

<script>
export default {
  data: function() {
    return {
      navbarExpanded: false
    };
  },
  methods: {
    toggleNavbar() {
      this.navbarExpanded = !this.navbarExpanded;
    },
    collapseNavbar() {
      this.navbarExpanded = false;
    }
  }
};
</script>
